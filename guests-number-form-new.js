function PlusButton() {
    var value = parseInt(document.getElementById('button').value, 10);
    value = isNaN(value) ? 0 : value;
    value++;
    document.getElementById('button').value = value;     
    
  }
  
  
  
  function MinusButton() {
    var value = parseInt(document.getElementById('button').value, 10);
    value = isNaN(value) ? 0 : value;
    value < 1 ? value = 1 : '';
    value--;
    document.getElementById('button').value = value;
  
  }


